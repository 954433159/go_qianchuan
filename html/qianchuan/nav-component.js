// 渲染导航
function renderNavigation() {
  const navContainer = document.getElementById('main-nav');
  if (!navContainer) return;

  const currentPath = window.location.pathname.split('/').pop();
  
  let html = '';
  navigationStructure.forEach(section => {
    html += `
      <div>
        <h3 class="mb-3 px-3 text-xs font-semibold text-gray-500 uppercase tracking-wider">${section.title}</h3>
        <ul class="space-y-1">
    `;
    
    section.items.forEach(item => {
      const isActive = currentPath === item.href;
      const activeClass = isActive 
        ? 'bg-gradient-to-r from-red-500 to-orange-500 text-white font-medium shadow-sm' 
        : 'text-gray-700 hover:bg-gray-100';
      
      const iconPath = iconMap[item.icon] || iconMap['default'];
      const badgeHtml = item.badge 
        ? `<span class="ml-auto px-2 py-0.5 text-xs font-medium bg-${item.badgeColor || 'gray'}-100 text-${item.badgeColor || 'gray'}-600 rounded-full">${item.badge}</span>`
        : '';
      
      html += `
        <li>
          <a href="${item.href}" class="group flex items-center gap-3 px-3 py-2.5 rounded-lg ${activeClass} transition-all">
            <svg class="w-5 h-5" fill="none" stroke="currentColor" viewBox="0 0 24 24">
              ${iconPath}
            </svg>
            <span>${item.name}</span>
            ${badgeHtml}
          </a>
        </li>
      `;
    });
    
    html += `
        </ul>
      </div>
    `;
  });
  
  navContainer.innerHTML = html;
}

// 页面加载时初始化
if (document.readyState === 'loading') {
  document.addEventListener('DOMContentLoaded', renderNavigation);
} else {
  renderNavigation();
}
